// Get orders grouped by date for picking
export const getDailyPickingLists = query({
  args: {
    token: v.string(),
    days: v.optional(v.number()), // How many days back to show
    shopDomain: v.optional(v.string()), // Filter by store
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }

    const daysBack = args.days ?? 30;
    const cutoffDate = new Date();
    cutoffDate.setDate(cutoffDate.getDate() - daysBack);
    const cutoffDateStr = cutoffDate.toISOString().split("T")[0];

    // --- Optimized: Use indexed date range instead of reading ALL orders ---
    let orders;
    if (args.shopDomain) {
      orders = await ctx.db
        .query("shopifyOrders")
        .withIndex("by_userId_shopDomain_placedOn", (q) =>
          q.eq("userId", user._id)
            .eq("shopDomain", args.shopDomain!)
            .gte("placedOn", cutoffDateStr)
        )
        .collect();
    } else {
      orders = await ctx.db
        .query("shopifyOrders")
        .withIndex("by_userId_placedOn", (q) =>
          q.eq("userId", user._id).gte("placedOn", cutoffDateStr)
        )
        .collect();
    }

    // Group by date
    const ordersByDate = new Map<string, typeof orders>();

    for (const order of orders) {
      if (order.placedOn >= cutoffDateStr) {
        const dateOrders = ordersByDate.get(order.placedOn) ?? [];
        dateOrders.push(order);
        ordersByDate.set(order.placedOn, dateOrders);
      }
    }

    // Convert to array and sort by date descending
    const dailyLists = Array.from(ordersByDate.entries())
      .map(([date, dateOrders]) => {
        // Calculate stats for this date
        const totalRevenue = dateOrders.reduce((sum, o) => sum + o.totalPrice, 0);
        const withAwb = dateOrders.filter((o) => o.trackingNumber).length;
        const withInvoice = dateOrders.filter((o) => o.invoiceNumber).length;
        const fulfilled = dateOrders.filter(
          (o) => o.fulfillmentStatus === "fulfilled"
        ).length;

        // Format display date (e.g., "27.01")
        const [_year, month, day] = date.split("-");
        const displayDate = `${day}.${month}`;

        return {
          date,
          displayDate,
          orderCount: dateOrders.length,
          totalRevenue,
          stats: {
            total: dateOrders.length,
            withAwb,
            withInvoice,
            fulfilled,
            pending: dateOrders.length - fulfilled,
          },
        };
      })
      .sort((a, b) => b.date.localeCompare(a.date));

    return dailyLists;
  },
});

// Get orders for a specific date (picking list detail)
export const getOrdersByDate = query({
  args: {
    token: v.string(),
    date: v.string(), // "YYYY-MM-DD"
    shopDomain: v.optional(v.string()), // Filter by store
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }

    // Get orders for this date
    let orders = await ctx.db
      .query("shopifyOrders")
      .withIndex("by_userId_placedOn", (q) =>
        q.eq("userId", user._id).eq("placedOn", args.date)
      )
      .collect();

    // Filter by store if specified
    if (args.shopDomain) {
      orders = orders.filter((o) => o.shopDomain === args.shopDomain);
    }

    // Work status is now denormalized directly on the order (isWorked, workedAt, etc.)

    // Calculate stats
    const stats = {
      totalOrders: orders.length,
      totalRevenue: orders.reduce((sum, o) => sum + o.totalPrice, 0),
      ordersWorked: orders.filter((o) => o.isWorked).length,
      awbsGenerated: orders.filter((o) => o.trackingNumber).length,
      invoicesCreated: orders.filter((o) => o.invoiceNumber).length,
    };

    // Aggregate products across all orders
    const productMap = new Map<
      string,
      { sku: string; name: string; quantity: number }
    >();
    orders.forEach((order) => {
      order?.items?.forEach((item: { sku?: string; name?: string; quantity?: number }) => {
        const key = item.sku || item.name || "unknown";
        const existing = productMap.get(key);
        if (existing) {
          existing.quantity += item.quantity || 1;
        } else {
          productMap.set(key, {
            sku: item.sku || "",
            name: item.name || "",
            quantity: item.quantity || 1,
          });
        }
      });
    });

    // Format display date
    const [_year, month, day] = args.date.split("-");
    const dateObj = new Date(args.date);
    const displayDate = dateObj.toLocaleDateString("en-US", {
      weekday: "long",
      year: "numeric",
      month: "long",
      day: "numeric",
    });

    return {
      date: args.date,
      displayDate,
      shortDate: `${day}.${month}`,
      orders,
      stats,
      aggregatedProducts: Array.from(productMap.values()).sort(
        (a, b) => b.quantity - a.quantity
      ),
    };
  },
});

