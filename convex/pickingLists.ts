// MIGRATION: Backfill shopDomain on existing picking lists
// ============================================

export const migrateBackfillShopDomain = mutation({
  args: {
    token: v.string(),
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }

    // Get all picking lists for this user that have no shopDomain
    const allLists = await ctx.db
      .query("pickingLists")
      .withIndex("by_userId", q => q.eq("userId", user._id))
      .collect();

    const listsWithoutDomain = allLists.filter(pl => !pl.shopDomain);

    let updated = 0;
    let skipped = 0;
    let empty = 0;

    for (const list of listsWithoutDomain) {
      // Get the orders in this picking list
      const items = await ctx.db
        .query("pickingListItems")
        .withIndex("by_pickingListId", q => q.eq("pickingListId", list._id))
        .collect();

      if (items.length === 0) {
        // Empty list — try to assign based on user's stores (use first/primary)
        // We'll skip these for now; they'll show up when no store filter is active
        skipped++;
        continue;
      }

      // Look at the first order to determine the shopDomain
      const firstOrder = await ctx.db.get(items[0].orderId);
      if (firstOrder?.shopDomain) {
        await ctx.db.patch(list._id, {
          shopDomain: firstOrder.shopDomain,
          updatedAt: Date.now(),
        });
        updated++;
      } else {
        empty++;
      }
    }

    return {
      total: listsWithoutDomain.length,
      updated,
      skipped,
      noShopDomain: empty,
    };
  },
});

