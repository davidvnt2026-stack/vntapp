// CRON: Create today's picking list for all users
// ============================================

export const createDailyPickingListsCron = internalMutation({
  args: {},
  handler: async (ctx) => {
    // Get all users
    const users = await ctx.db.query("profiles").collect();
    
    // Format today's date
    const today = new Date();
    const day = today.getDate().toString().padStart(2, "0");
    const month = (today.getMonth() + 1).toString().padStart(2, "0");
    const year = today.getFullYear();
    const todayName = `Picking List ${day}.${month}.${year}`;
    
    let created = 0;
    let skipped = 0;
    
    for (const user of users) {
      // Check if today's list already exists for this user
      const existingLists = await ctx.db
        .query("pickingLists")
        .withIndex("by_userId", (q) => q.eq("userId", user._id))
        .collect();
      
      const existingToday = existingLists.find((pl) => pl.name === todayName);
      
      if (!existingToday) {
        // Create new picking list for today
        await ctx.db.insert("pickingLists", {
          userId: user._id,
          name: todayName,
          status: "pending",
          createdAt: Date.now(),
          updatedAt: Date.now(),
        });
        created++;
      } else {
        skipped++;
      }
    }
    
    console.log(`Daily picking lists cron: created ${created}, skipped ${skipped} (already existed)`);
    return { created, skipped };
  },
});