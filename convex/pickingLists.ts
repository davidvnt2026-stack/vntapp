// Get or create today's picking list
export const getOrCreateToday = mutation({
  args: {
    token: v.string(),
    shopDomain: v.optional(v.string()), // Which store
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }
    
    // Format today's date
    const today = new Date();
    const day = today.getDate().toString().padStart(2, "0");
    const month = (today.getMonth() + 1).toString().padStart(2, "0");
    const year = today.getFullYear();
    const todayName = `Picking List ${day}.${month}.${year}`;
    
    // Check if today's list already exists for this store
    let pickingLists;
    if (args.shopDomain) {
      pickingLists = await ctx.db
        .query("pickingLists")
        .withIndex("by_userId_shopDomain", q => q.eq("userId", user._id).eq("shopDomain", args.shopDomain))
        .collect();
    } else {
      pickingLists = await ctx.db
        .query("pickingLists")
        .withIndex("by_userId", q => q.eq("userId", user._id))
        .collect();
    }
    
    const existingToday = pickingLists.find(pl => pl.name === todayName);
    
    if (existingToday) {
      return { id: existingToday._id, name: existingToday.name, created: false };
    }
    
    // Create new picking list for today
    const id = await ctx.db.insert("pickingLists", {
      userId: user._id,
      shopDomain: args.shopDomain,
      name: todayName,
      status: "pending",
      createdAt: Date.now(),
      updatedAt: Date.now(),
    });
    
    return { id, name: todayName, created: true };
  },
});

// Add single order to picking list (with auto-create today option)
export const addSingleOrder = mutation({
  args: {
    token: v.string(),
    orderId: v.id("shopifyOrders"),
    pickingListId: v.optional(v.id("pickingLists")),
    useToday: v.optional(v.boolean()),
    shopDomain: v.optional(v.string()), // Which store (used when auto-creating today's list)
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }
    
    // Verify order belongs to user
    const order = await ctx.db.get(args.orderId);
    if (!order || order.userId !== user._id) {
      throw new ConvexError("Comanda nu a fost găsită.");
    }
    
    // Use the order's shopDomain if not explicitly provided
    const shopDomain = args.shopDomain || order.shopDomain;
    
    let pickingListId = args.pickingListId;
    let pickingListName = "";
    
    // If useToday, get or create today's list for this store
    if (args.useToday && !pickingListId) {
      const today = new Date();
      const day = today.getDate().toString().padStart(2, "0");
      const month = (today.getMonth() + 1).toString().padStart(2, "0");
      const year = today.getFullYear();
      const todayName = `Picking List ${day}.${month}.${year}`;
      
      // Find existing today's list for this store
      let pickingLists;
      if (shopDomain) {
        pickingLists = await ctx.db
          .query("pickingLists")
          .withIndex("by_userId_shopDomain", q => q.eq("userId", user._id).eq("shopDomain", shopDomain))
          .collect();
      } else {
        pickingLists = await ctx.db
          .query("pickingLists")
          .withIndex("by_userId", q => q.eq("userId", user._id))
          .collect();
      }
      
      const existingToday = pickingLists.find(pl => pl.name === todayName);
      
      if (existingToday) {
        pickingListId = existingToday._id;
        pickingListName = existingToday.name;
      } else {
        pickingListId = await ctx.db.insert("pickingLists", {
          userId: user._id,
          shopDomain,
          name: todayName,
          status: "pending",
          createdAt: Date.now(),
          updatedAt: Date.now(),
        });
        pickingListName = todayName;
      }
    }
    
    if (!pickingListId) {
      throw new ConvexError("Nu a fost specificat un picking list.");
    }
    
    // Verify picking list belongs to user
    const list = await ctx.db.get(pickingListId);
    if (!list || list.userId !== user._id) {
      throw new ConvexError("Lista de picking nu a fost găsită.");
    }
    pickingListName = pickingListName || list.name;
    
    // Check if already in list
    const existingItem = await ctx.db
      .query("pickingListItems")
      .withIndex("by_pickingListId", q => q.eq("pickingListId", pickingListId!))
      .filter(q => q.eq(q.field("orderId"), args.orderId))
      .first();
    
    if (existingItem) {
      return { success: true, alreadyExists: true, pickingListName };
    }
    
    // Add to list
    await ctx.db.insert("pickingListItems", {
      pickingListId,
      orderId: args.orderId,
      createdAt: Date.now(),
    });
    
    // Update list timestamp
    await ctx.db.patch(pickingListId, {
      updatedAt: Date.now(),
    });
    
    return { success: true, alreadyExists: false, pickingListName };
  },
});

export const addOrders = mutation({
  args: {
    token: v.string(),
    pickingListId: v.id("pickingLists"),
    orderIds: v.array(v.id("shopifyOrders")),
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }
    
    const list = await ctx.db.get(args.pickingListId);
    if (!list || list.userId !== user._id) {
      throw new ConvexError("Lista de picking nu a fost găsită.");
    }
    
    // Get existing items to avoid duplicates
    const existingItems = await ctx.db
      .query("pickingListItems")
      .withIndex("by_pickingListId", q => q.eq("pickingListId", args.pickingListId))
      .collect();
    
    const existingOrderIds = new Set(existingItems.map(i => i.orderId));
    
    // Add new orders
    let addedCount = 0;
    let skippedCount = 0;
    for (const orderId of args.orderIds) {
      if (!existingOrderIds.has(orderId)) {
        // Verify order belongs to user
        const order = await ctx.db.get(orderId);
        if (order && order.userId === user._id) {
          await ctx.db.insert("pickingListItems", {
            pickingListId: args.pickingListId,
            orderId,
            createdAt: Date.now(),
          });
          addedCount++;
        }
      } else {
        skippedCount++;
      }
    }
    
    // Update list timestamp
    await ctx.db.patch(args.pickingListId, {
      updatedAt: Date.now(),
    });
    
    return { addedCount, skippedCount, pickingListName: list.name };
  },
});

