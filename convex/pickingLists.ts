// Get order -> picking list mappings only for a specific set of orders
export const getOrderPickingListMappingsForOrders = query({
  args: {
    token: v.string(),
    orderIds: v.array(v.id("shopifyOrders")),
    shopDomain: v.optional(v.string()),
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }

    if (args.orderIds.length === 0) {
      return [] as { orderId: string; pickingListId: string; pickingListName: string }[];
    }

    const listCache = new Map<string, { _id: string; name: string; shopDomain?: string } | null>();
    const mappings: { orderId: string; pickingListId: string; pickingListName: string }[] = [];

    for (const orderId of args.orderIds) {
      const items = await ctx.db
        .query("pickingListItems")
        .withIndex("by_orderId", (q) => q.eq("orderId", orderId))
        .collect();

      for (const item of items) {
        const cacheKey = item.pickingListId as string;
        let list = listCache.get(cacheKey);
        if (list === undefined) {
          const found = await ctx.db.get(item.pickingListId);
          if (!found || found.userId !== user._id) {
            list = null;
          } else {
            list = {
              _id: found._id,
              name: found.name,
              shopDomain: found.shopDomain,
            };
          }
          listCache.set(cacheKey, list);
        }

        if (!list) continue;
        if (args.shopDomain && list.shopDomain !== args.shopDomain) continue;

        mappings.push({
          orderId: item.orderId,
          pickingListId: list._id,
          pickingListName: list.name,
        });
      }
    }

    return mappings;
  },
});

