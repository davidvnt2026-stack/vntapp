export const listOrdersPaginated = query({
  args: {
    token: v.string(),
    id: v.id("pickingLists"),
    paginationOpts: paginationOptsValidator,
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }

    const list = await ctx.db.get(args.id);
    if (!list || list.userId !== user._id) {
      return {
        page: [],
        isDone: true,
        continueCursor: "",
      };
    }

    const result = await ctx.db
      .query("pickingListItems")
      .withIndex("by_pickingListId", (q) => q.eq("pickingListId", args.id))
      .order("desc")
      .paginate(args.paginationOpts);

    const orderPromises = result.page.map((item) => ctx.db.get(item.orderId));
    const rawOrders = await Promise.all(orderPromises);

    const page = result.page.map((item, i) => {
      const order = rawOrders[i];
      if (!order || order.userId !== user._id) return null;
      return {
        ...projectOrderForPickingList(order),
        pickingListItemId: item._id,
      };
    });

    return {
      ...result,
      page: page.filter((o) => o !== null),
    };
  },
});

