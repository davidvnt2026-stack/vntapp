export const getWithOrders = query({
  args: {
    token: v.string(),
    id: v.id("pickingLists"),
  },
  handler: async (ctx, args) => {
    const user = await getUserFromToken(ctx, args.token);
    if (!user) {
      throw new ConvexError("Sesiune expirată. Te rugăm să te autentifici din nou.");
    }
    
    const list = await ctx.db.get(args.id);
    if (!list || list.userId !== user._id) {
      return null;
    }
    
    // Get all items in this list
    const listItems = await ctx.db
      .query("pickingListItems")
      .withIndex("by_pickingListId", q => q.eq("pickingListId", args.id))
      .collect();
    
    // Get orders for each item
    const orderPromises = listItems.map((item) => ctx.db.get(item.orderId));
    const rawOrders = await Promise.all(orderPromises);

    const orders = rawOrders.map((order, i) => {
      if (!order) return null;
      
      // Work status is now denormalized directly on the order
      return {
        ...projectOrderForPickingList(order),
        pickingListItemId: listItems[i]._id,
        // isWorked, workedAt, workedBy, workedByName are already on the order
      };
    });
    
    // Filter out null orders
    const validOrders = orders.filter(o => o !== null);
    
    // Calculate stats
    const stats = {
      totalOrders: validOrders.length,
      totalRevenue: validOrders.reduce((sum, o) => sum + (o?.totalPrice || 0), 0),
      ordersWorked: validOrders.filter(o => o?.isWorked).length,
      awbsGenerated: validOrders.filter(o => o?.trackingNumber).length,
      invoicesCreated: validOrders.filter(o => o?.invoiceNumber).length,
    };
    
    // Aggregate products
    const productMap = new Map<string, { sku: string; name: string; quantity: number }>();
    validOrders.forEach(order => {
      order?.items?.forEach((item: any) => {
        const key = item.sku || item.name;
        const existing = productMap.get(key);
        if (existing) {
          existing.quantity += item.quantity || 1;
        } else {
          productMap.set(key, {
            sku: item.sku || "",
            name: item.name || "",
            quantity: item.quantity || 1,
          });
        }
      });
    });
    
    return {
      ...list,
      orders: validOrders,
      stats,
      aggregatedProducts: Array.from(productMap.values()),
    };
  },
});

